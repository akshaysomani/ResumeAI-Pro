/**
 * Translates a key for a given locale. Falls back to English if missing.
 */
export function getTranslation(locale: string, key: string): string {
  const dict = TRANSLATIONS[locale] || TRANSLATIONS.en;
  return dict[key] || TRANSLATIONS.en[key] || key;
}

/**
 * Formats monetary amounts across global currency standards.
 */
export function formatCurrency(amount: number, currency: string, locale: string = "en-US"): string {
  try {
    return new Intl.NumberFormat(locale, {
      style: "currency",
      currency,
    }).format(amount);
  } catch (e) {
    return `${currency} ${amount.toFixed(2)}`;
  }
}

/**
 * Formats Date object into localized structures.
 */
export function formatDate(date: string | Date, locale: string = "en-US"): string {
  try {
    return new Intl.DateTimeFormat(locale, {
      dateStyle: "medium",
    }).format(new Date(date));
  } catch (e) {
    return String(date);
  }
}
